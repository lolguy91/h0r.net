void ISR_RegisterHandler(int irq, ISRHandler handler) {
    ISRHandlers[irq] = handler;
}
void ISR_Handler(Registers *regs) {
    if (regs->interrupt < 32) {
        trigger_psod(HN_ERR_HW_EXCEPTION, err_strings[regs->interrupt], regs);
        return;
    }
    if (ISRHandlers[regs->interrupt - 32] != 0) {
        ISRHandlers[regs->interrupt - 32](regs);
    } else {
        log_error("ISR Handler #%i used while not present", regs->interrupt);
    }
}

